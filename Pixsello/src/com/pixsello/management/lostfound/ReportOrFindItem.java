public class ReportOrFindItem {
	
	private String reportDate;
	
	private String reportTime;
	
	private String reportDesc;
	
	private String reportLocation;
	
	private String reportRoomNum;
	
	private String reportStaff;
	
	public String getReportDate() {
		return reportDate;
	}

	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}

	public String getReportTime() {
		return reportTime;
	}

	public void setReportTime(String reportTime) {
		this.reportTime = reportTime;
	}

	public String getReportDesc() {
		return reportDesc;
	}

	public void setReportDesc(String reportDesc) {
		this.reportDesc = reportDesc;
	}

	public String getReportLocation() {
		return reportLocation;
	}

	public void setReportLocation(String reportLocation) {
		this.reportLocation = reportLocation;
	}

	public String getReportRoomNum() {
		return reportRoomNum;
	}

	public void setReportRoomNum(String reportRoomNum) {
		this.reportRoomNum = reportRoomNum;
	}

	public String getReportStaff() {
		return reportStaff;
	}

	public void setReportStaff(String reportStaff) {
		this.reportStaff = reportStaff;
	}

	public String getStayDateFrom() {
		return stayDateFrom;
	}

	public void setStayDateFrom(String stayDateFrom) {
		this.stayDateFrom = stayDateFrom;
	}

	public String getStayDateTo() {
		return stayDateTo;
	}

	public void setStayDateTo(String stayDateTo) {
		this.stayDateTo = stayDateTo;
	}

	private String stayDateFrom;
	
	private String stayDateTo;
	
	
	

}
